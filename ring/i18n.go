package ring

import "fmt"

// Tr looks up key for lang (falling back to English) and formats args in. The
// language codes follow ReTouch's UI language setting (en/de/nl/fr/es/af).
// Shared by the OLED notifications here and the plugin's settings manifest.
func Tr(lang, key string, args ...any) string {
	m, ok := i18n[lang]
	if !ok {
		m = i18n["en"]
	}
	s, ok := m[key]
	if !ok {
		s = i18n["en"][key]
	}
	if len(args) > 0 {
		return fmt.Sprintf(s, args...)
	}
	return s
}

